public class If {
    public static void main(String[] args) {
        int value = 5;

        if (value > 20) {
            System.out.println("Да, верно.");
        } else if (value < 0){
            System.out.println("Нет, не верно.");
        } else{
            System.out.println("Ничего из данного не верно");
        }
    }
}
