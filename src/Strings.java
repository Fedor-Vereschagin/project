public class Strings {
    public static void main(String[] args) {
        int myInt = 314;
        String myString = "Hello";
        String space = " ";
        String name = "Fedor";
        System.out.println(myString + space + name);
        System.out.println("Hello" + " " + "world");
        System.out.println("My number is " + myInt);
    }
}
